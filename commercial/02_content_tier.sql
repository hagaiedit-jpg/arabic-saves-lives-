-- עמודת דרג לכל פריט תוכן. ברירת מחדל 'free' => שינוי לא שובר כלום.
alter table public.phrases
  add column if not exists tier text not null default 'free';

-- ----------------------------------------------------------------------------
--  אסטרטגיית ה"טעימה" (preview): מה נשאר חינם ומה הופך לבתשלום.
--  זו החלטה עסקית שלך — ערכי את התנאים למטה.
--  דוגמה: משאירים חינם את מדור החירום + קטגוריית מילון אחת כטעימה,
--  וכל השאר הופך ל-premium.
-- ----------------------------------------------------------------------------

-- 1) כברירת מחדל — הכול חינם (כבר קבוע ע"י ה-default למעלה). כלום לא ננעל עדיין.

-- 2) מסמנים כ-premium את מה שיידרש מנוי כדי לראות. דוגמאות (ערכי לפי בחירתך):
--    כל המילון המבצעי חוץ מקטגוריית טעימה אחת:
update public.phrases
   set tier = 'premium'
 where section = 'dictionary'
   and cat <> 'צ׳ק פוסט ומחסום';         -- הקטגוריה הזו נשארת טעימה חינמית

--    התרחישים המלאים — בתשלום:
update public.phrases
   set tier = 'premium'
 where section = 'scenario';

